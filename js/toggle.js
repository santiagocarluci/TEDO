$(document).ready(function(){
    $('#mostrar-comedores').toggle(
    function(e){
      $('.comedores-oculto').slideDown();
      $(this).text('VER MENOS');
      e.preventDefault();
    },
    function(e){
      $('.comedores-oculto').slideUp();
      $(this).text('VER MÁS');
      e.preventDefault();
    }
  );
});

